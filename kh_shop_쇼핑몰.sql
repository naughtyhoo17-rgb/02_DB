-- 3.
SELECT ORDER_ID 주문번호, PRODUCT_NAME 상품명, QUANTITY 수량,
PRICE_PER_UNIT 개별금액, (QUANTITY * PRICE_PER_UNIT) 주문별금액합계
FROM ORDER_DETAILS JOIN PRODUCTS USING(PRODUCT_ID)
WHERE ORDER_ID IN
(SELECT ORDER_ID FROM ORDERS WHERE CUSTOMER_ID =
(SELECT CUSTOMER_ID FROM CUSTOMERS WHERE NAME = '유관순'))
ORDER BY 3 DESC;










